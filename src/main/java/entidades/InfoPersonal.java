package entidades;

public class InfoPersonal {

    private Solicitante solicitante;
    private InstitucionEducativa institucionEducativa;
    private ProgramaSocial programaSocial;
    private DependenciaVivienda dependenciaVivienda;
    private TrabajoSolicitante trabajoSolicitante;
    private GastosForaneo gastosForaneo;

    public Solicitante getSolicitante() {
        return solicitante;
    }

    public void setSolicitante(Solicitante solicitante) {
        this.solicitante = solicitante;
    }

    public InstitucionEducativa getInstitucionEducativa() {
        return institucionEducativa;
    }

    public void setInstitucionEducativa(InstitucionEducativa institucionEducativa) {
        this.institucionEducativa = institucionEducativa;
    }

    public ProgramaSocial getProgramaSocial() {
        return programaSocial;
    }

    public void setProgramaSocial(ProgramaSocial programaSocial) {
        this.programaSocial = programaSocial;
    }

    public DependenciaVivienda getDependenciaVivienda() {
        return dependenciaVivienda;
    }

    public void setDependenciaVivienda(DependenciaVivienda dependenciaVivienda) {
        this.dependenciaVivienda = dependenciaVivienda;
    }

    public TrabajoSolicitante getTrabajoSolicitante() {
        return trabajoSolicitante;
    }

    public void setTrabajoSolicitante(TrabajoSolicitante trabajoSolicitante) {
        this.trabajoSolicitante = trabajoSolicitante;
    }

    public GastosForaneo getGastosForaneo() {
        return gastosForaneo;
    }

    public void setGastosForaneo(GastosForaneo gastosForaneo) {
        this.gastosForaneo = gastosForaneo;
    }

    
}
